# List 메모리 저장 방식 예제

a = [5,4,2,3,1]
b = [1,2,3,4,5]

print(a)
print(b)
b = a
# b = a는 a리스트 주소값을 b로 할당하라는 의미로
# 이는 곧 a리스트 주소값을 b도 가르킨다는 의미
print(b)

a.sort()
# sort 함수는 a 리스트 멤버 값을 순차적으로 재 배열하는 함수
print(b)
# 이 때 b를 출력하면 b가 a 리스트 주소값을 가르키고 있으므로
# 재 배열된 리스트 값을 출력 하게됨

b = [6,7,8,9,10]
# 이 후 b라는 리스트에 새로운 변수 값을 입력하거나 접근 하면
# b와 a 간을 링크가 깨어지고 새로운 b만의 주소가 할당됨
print(a,b)
