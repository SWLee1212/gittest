# for, while 문 예제

for looper in [1,2,3,4,5]:          # list 변수를 만들고 이를 순차적으로 looper에 할당
    #print("Hello")
    print(looper)

for looper in range(1,10):
    print(looper)

for looper in range(10,1):
    print(looper)

for looper in range(10,0,-3):
    print(looper)

for looper in 'abcdefg':
    print(looper)

for looper in ['abcdefg', 'ghgklm', 'opqrstu']:
    print(looper)
