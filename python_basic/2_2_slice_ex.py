# slice 기법 예저

cities = ["서울","대전", "대구", "광주", \
  "부산","강원","수원","인천"]      # \를 하면 다음 줄에 이어서 코드 작성 가능

print(cities[0:6] , len(cities[0:6]))
# list 변수에 특정 변수 접근 변수명[숫자] 임
# cities[0:6] 을 하면 0번째 변수부터 5번째 변수를 가리킴

print(cities[:])
# list의 모든 변수 변수에 접근 [:]

print(cities[-20:20])
# 변수 인덱스 번호를 벗어나는 접근인 경우 자동으로 변수 인덱스에 맞도록 출력 가능
print(cities[::2], "AND",cities[::-1])
print(cities[::5], "AND",cities[::-2])
# ::2를 0,2,4,... 로 인덱스 접근
# ::-1를 가장 마지막 인덱스에서 -1씩 인덱스 접근
